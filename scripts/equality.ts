/* Чому код поводиться таким чином? */

const isBig = (thing) => {
    if (thing == 0 || thing == 1 || thing == 2) {
        return false;
    }
    return true;
};
isBig(1); // false, в даному випадку задовольняється умова 1 == 1
isBig([2]); // false, задовільняється умова [2] == 2, бо число у масиві приводиться до звичайного числа
isBig([3]); // true, 3 не дорівнює 0, 1 і 2
